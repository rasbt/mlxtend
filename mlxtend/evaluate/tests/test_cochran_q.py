import numpy as np

from mlxtend.evaluate import cochrans_q, mcnemar, mcnemar_table
from mlxtend.utils import assert_raises


def test_input_array_1d():
    t = np.array([[1, 2], [3, 4]])
    assert_raises(
        ValueError,
        "One or more input arrays are not 1-dimensional.",
        cochrans_q,
        t,
        t,
        t,
    )


def test_input_array_lengths_1():
    t = np.array([1, 2])
    t2 = np.array([1, 2, 3])
    assert_raises(
        ValueError,
        ("Each prediction array must have" " the same number of samples."),
        cochrans_q,
        t,
        t2,
        t,
    )


def test_model_have_same_len():
    y_true = np.array([1, 1, 0])
    y_1 = np.array([0, 1, 0])
    y_2 = np.array([1, 1, 0])
    y_3 = np.array([1, 1])

    assert_raises(
        ValueError,
        ("Each prediction array must have" " the same number of samples."),
        cochrans_q,
        y_true,
        y_1,
        y_2,
        y_3,
    )


def test_min_number_of_models():
    y_true = np.array([1, 1, 0])
    y_1 = np.array([0, 1, 0])

    assert_raises(
        ValueError,
        ("Provide at least 2 model prediction arrays."),
        cochrans_q,
        y_true,
        y_1,
    )


